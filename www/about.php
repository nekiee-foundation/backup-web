              <p>Essentially, we are who we are because of where we have been born into and our life takes its course because of this. A child born into a well to do family has a much better chance of doing well in life as compared to someone from an under-privileged section of the society.This of course is not a rule but a generalized trend although there are umpteen number of exceptions to this. Can we, who have the means to help others not as fortunate as us,pledge to making a difference?</p>
