                On one hand, we have the cosmopolitan image of India bustling with economic activity and the fast-paced lives and just around the corner, both within urban poor and the village life, the contrasting image of India is seen which is still mired by caste based discrimination and economic disparity. This is one of the biggest triggers for Neikee to have been conceptualized.
              </p>
              <p>
                There are many small NGOs, individuals and self-help groups doing fantastic work at the grass root levels across our country however the impact that they create, generally remains limited due to scarcity of funds available to them. Similarly, a lot of people want to donate money for good causes but don’t do so at times due to lack of transparency and authenticity around where they should donate and how their donations would ultimately be used.
                <br>
                The intent with which Neikee was conceptualized was to bridge this gap by supporting innovative campaigns and in turn, help create a much larger impact.
